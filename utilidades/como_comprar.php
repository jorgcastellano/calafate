<li>   Seleccionar los articulos que se quieran comprar y elegir la cantidad de los mismos y hacer click en "Agregar al carrito". Hacer esto para los diferentes articulos.</li>
<li>   Una vez terminada la seleccion, hacer click en "Terminar Compra".</li>
<li>   Revisar el pedido: Dede aca puede ver si esta todo como usted eligio en articulos y cantidades. Si desea modificarlos o eliminarlos debe clickear en las opciones correspondientes y luego hacer click en "Modificar el carrito".</li>
<li>   Si se arrepintió de la compra puede hacer click en "Vaciar carrito".</li>
<li>   Si todo esta bien debe elegir si desea retirar la mercaderia en nuestro domicilio o si desea recibirla en el suyo. En este caso el envio se hace mediante CORREO ARGENTINO y tiene un costo de $35 (pesos argentinos treinta y cinco).</li>
<li>   Luego hacer click en "confirmar comprar" para generar la orden de compra.</li>
<li>   Ahora solo resta efectuar el pago. Para una mayor seguridad y comodidad el mismo se efectua mediante la empresa "MERCADO PAGO" que es parte del mayor sitio de ventas por internet que es MERCADO LIBRE. Para poder efectuarlo debe estar registrado en cualquiera de las dos (mercado pago o mercado libre). No es necesario que se registre con anterioridad: A la hora de pagar puede hacerlo y ya quede registrado para futuras transacciones.</li>
<li>   Puede elegir abonar con tarjeta de credito, en cuotas, en pago via transferencia bancaria o imprimir cupones de pago para PAGO FACIL o BAPRO PAGOS.


